def write_episode_cast_roles(vault: Path, episode_id: str, roles: list[CastRole]) -> None:
    episode = ensure_episode(vault, episode_id)
    meta = episode_meta(episode)
    meta["cast_overrides"] = [
        {"kind": role.kind, "person": role.person, **({"plays": role.plays} if role.plays else {})}
        for role in roles
    ]
    write_episode_meta(episode, meta)


def write_cast_roles(vault: Path, roles: list[CastRole]) -> None:
    pack_path = vault / "pack.yaml"
    pack = pack_path.read_text(encoding="utf-8")
    role_lines = ["roles:"]
    for role in roles:
        role_lines.append(f"  - kind: {role.kind}")
        role_lines.append(f"    person: {role.person}")
        if role.plays:
            role_lines.append(f"    plays: {role.plays}")
    replacement = "\n".join(role_lines)
    atomic_write_text(pack_path, _replace_section(pack, "roles:", replacement, stop_prefixes=("provider:", "budget:")))


