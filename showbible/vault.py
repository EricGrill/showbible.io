def _section_lines(text: str, header: str, stop_prefixes: tuple[str, ...]) -> list[str]:
    lines = text.splitlines()
    start = next((index for index, line in enumerate(lines) if line.strip() == header), None)
    if start is None:
        return []
    section: list[str] = []
    for line in lines[start + 1 :]:
        if line and not line.startswith(" ") and line.strip().endswith(":") and line.strip() in stop_prefixes:
            break
        section.append(line)
    return section


def _replace_section(text: str, header: str, replacement: str, stop_prefixes: tuple[str, ...]) -> str:
    lines = text.splitlines()
    start = next((index for index, line in enumerate(lines) if line.strip() == header), None)
    if start is None:
        return text.rstrip() + "\n" + replacement + "\n"
    end = len(lines)
    for index in range(start + 1, len(lines)):
        line = lines[index]
        if line and not line.startswith(" ") and line.strip().endswith(":") and line.strip() in stop_prefixes:
            end = index
            break
    return "\n".join([*lines[:start], replacement, *lines[end:]]).rstrip() + "\n"


def _frontmatter_value(text: str, key: str) -> str | None:
    match = re.search(rf"^{re.escape(key)}:\s*(.+)$", text, flags=re.MULTILINE)
    if not match:
        return None
    return match.group(1).strip().strip("\"'")


def ensure_episode(vault: Path, episode_id: str) -> Path:
    episode = vault / "episodes" / episode_id
    for directory in ["writers-room", "drafts"]:
        (episode / directory).mkdir(parents=True, exist_ok=True)
    meta = episode / "meta.json"
    if not meta.exists():
        atomic_write_json(
            meta,
            {
                "schema": 1,
                "episode": episode_id,
                "status": "created",
                "completed_phases": [],
                "phase_events": [],
                "interventions": [],
            },
        )
    return episode


def list_episodes(vault: Path) -> list[str]:
    episodes_dir = vault / "episodes"
    if not episodes_dir.exists():
        return []
    return sorted(path.name for path in episodes_dir.iterdir() if path.is_dir())


def episode_meta(episode: Path) -> dict[str, Any]:
    return read_json(
        episode / "meta.json",
        {
            "schema": 1,
            "episode": episode.name,
            "status": "created",
            "completed_phases": [],
            "phase_events": [],
            "interventions": [],
        },
    )


def write_episode_meta(episode: Path, meta: dict[str, Any]) -> None:
    atomic_write_json(episode / "meta.json", meta)


def append_transcript_entry(path: Path, author: str, role: str, body: str, intervention: bool = False) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not path.exists():
        atomic_write_text(path, "---\nschema: 1\n---\n\n")
    stamp = _next_stamp(path)
    suffix = ", intervention" if intervention else ""
    existing = path.read_text(encoding="utf-8")
    entry = f"## {author} ({role}{suffix}) - {stamp}\n{body.strip()}\n\n"
    atomic_write_text(path, existing.rstrip() + "\n\n" + entry)


def _next_stamp(path: Path) -> str:
    existing = path.read_text(encoding="utf-8") if path.exists() else ""
    count = existing.count("\n## ") + (1 if existing.startswith("## ") else 0)
    second = count % 60
    minute = (count // 60) % 60
    return f"00:{minute:02d}:{second:02d}"


def doctor(vault: Path) -> list[DoctorFinding]:
    findings: list[DoctorFinding] = []
    required = [
        "pack.yaml",
        "people",
        "lore-bible/canon.md",
        "lore-bible/glossary.md",
        "arcs",
        "episodes",
        ".room",
    ]
    for item in required:
        path = vault / item
        if not path.exists():
            findings.append(DoctorFinding("error", item, "Required vault path is missing."))
    if (vault / "people").exists() and not list((vault / "people").glob("*.md")):
        findings.append(DoctorFinding("error", "people", "At least one personality file is required."))
    pack = vault / "pack.yaml"
    if pack.exists() and "schema:" not in pack.read_text(encoding="utf-8"):
        findings.append(DoctorFinding("error", "pack.yaml", "pack.yaml must declare schema."))
    return findings


def arc_beats(vault: Path) -> list[ArcBeat]:
    arcs_dir = vault / "arcs"
    if not arcs_dir.is_dir():
        return []
    pattern = re.compile(r"^-\s*(S\d+E\d+)\s+\[([^\]]+)\]\s+(.+)$", flags=re.IGNORECASE)
    results: list[ArcBeat] = []
    for path in sorted(arcs_dir.glob("*.md")):
        slug = path.stem
        for raw in path.read_text(encoding="utf-8").splitlines():
            match = pattern.match(raw.strip())
            if match:
                results.append(
                    ArcBeat(
                        arc=slug,
                        episode=match.group(1).upper(),
                        status=match.group(2).strip(),
                        beat=match.group(3).strip(),
                        file=path,
                    )
                )
    return results


def _arc_file(vault: Path, arc_slug: str) -> Path:
    return vault / "arcs" / f"{slugify(arc_slug)}.md"


def _arc_line(episode_id: str, status: str, beat: str) -> str:
    return f"- {episode_id.upper()} [{status.strip()}] {beat.strip()}"


def add_arc_beat(vault: Path, arc_slug: str, episode_id: str, status: str, beat: str) -> Path:
    path = _arc_file(vault, arc_slug)
    if not path.exists():
        title = arc_slug.replace("-", " ").title()
        atomic_write_text(path, f"# {title}\n\n")
    body = path.read_text(encoding="utf-8")
    if "## Episode Beats" not in body:
        body = body.rstrip() + "\n\n## Episode Beats\n\n"
    elif not body.endswith("\n"):
        body += "\n"
    atomic_write_text(path, body + _arc_line(episode_id, status, beat) + "\n")
    return path


def update_arc_beat(
    vault: Path,
    *,
    arc_slug: str,
    episode_id: str,
    original_beat: str,
    new_episode_id: str,
    new_status: str,
    new_beat: str,
) -> Path:
    path = _arc_file(vault, arc_slug)
    if not path.exists():
        raise VaultError(f"arc not found: {arc_slug}")
    text = path.read_text(encoding="utf-8")
    new_text, count = _replace_arc_line(text, episode_id, original_beat, _arc_line(new_episode_id, new_status, new_beat))
    if count == 0:
        raise VaultError(f"arc beat not found in {arc_slug}: {episode_id} {original_beat!r}")
    atomic_write_text(path, new_text)
    return path


def remove_arc_beat(vault: Path, arc_slug: str, episode_id: str, beat: str) -> Path:
    path = _arc_file(vault, arc_slug)
    if not path.exists():
        raise VaultError(f"arc not found: {arc_slug}")
    text = path.read_text(encoding="utf-8")
    new_text, count = _replace_arc_line(text, episode_id, beat, replacement=None)
    if count == 0:
        raise VaultError(f"arc beat not found in {arc_slug}: {episode_id} {beat!r}")
    atomic_write_text(path, new_text)
    return path


def _replace_arc_line(
    text: str,
    episode_id: str,
    beat: str,
    replacement: str | None,
) -> tuple[str, int]:
    pattern = re.compile(
        rf"^-\s*(?i:{re.escape(episode_id.upper())})\s+\[[^\]]+\]\s+{re.escape(beat.strip())}[ \t]*$",
        flags=re.MULTILINE,
    )
    if replacement is None:
        new_text, count = pattern.subn("", text)
        new_text = re.sub(r"\n{3,}", "\n\n", new_text)
        return new_text, count
    return pattern.subn(replacement, text)


def copy_episode(vault: Path, source_id: str, target_id: str) -> Path:
    source = vault / "episodes" / source_id
    target = vault / "episodes" / target_id
    if not source.exists():
        raise VaultError(f"Episode not found: {source_id}")
    if target.exists():
        raise VaultError(f"Episode already exists: {target_id}")
    shutil.copytree(source, target)
    meta = episode_meta(target)
    meta["episode"] = target_id
    meta["forked_from"] = source_id
    write_episode_meta(target, meta)
    return target


_LORE_FACT_RE = re.compile(
    r"^-\s*(?:\*\*Manual fact\*\*\s*-\s*)?(.+?)(?:\s*\*Source:\s*([^*]+?)\s*\*)?\s*$"
)


def lore_facts(vault: Path) -> list[LoreFact]:
    canon = vault / "lore-bible" / "canon.md"
    if not canon.exists():
        return []
    in_facts = False
    results: list[LoreFact] = []
    for raw in canon.read_text(encoding="utf-8").splitlines():
        line = raw.rstrip()
        stripped = line.lstrip()
        if stripped.startswith("## "):
            in_facts = stripped.lower().startswith("## facts")
            continue
        if not in_facts:
            continue
        match = _LORE_FACT_RE.match(stripped)
        if not match:
            continue
        text = match.group(1).strip()
        source = (match.group(2) or "manual").strip()
        if text:
            results.append(LoreFact(text=text, source=source, file=canon))
    return results


def _canon_path(vault: Path) -> Path:
    return vault / "lore-bible" / "canon.md"


def _lore_line(text: str, source: str) -> str:
    return f"- **Manual fact** - {text.strip()} *Source: {source.strip() or 'manual'}*"


def add_lore_fact(vault: Path, text: str, *, source: str = "manual") -> Path:
    path = _canon_path(vault)
    body = path.read_text(encoding="utf-8") if path.exists() else "# Canon\n\n## Facts\n\n"
    if "## Facts" not in body:
        body = body.rstrip() + "\n\n## Facts\n\n"
    body = body.rstrip() + "\n" + _lore_line(text, source) + "\n"
    atomic_write_text(path, body)
    return path


def update_lore_fact(
    vault: Path,
    *,
    original_text: str,
    new_text: str,
    new_source: str,
) -> Path:
    path = _canon_path(vault)
    if not path.exists():
        raise VaultError("canon.md missing")
    text = path.read_text(encoding="utf-8")
    new_body, count = _replace_lore_line(text, original_text, _lore_line(new_text, new_source))
    if count == 0:
        raise VaultError(f"lore fact not found: {original_text!r}")
    atomic_write_text(path, new_body)
    return path


def remove_lore_fact(vault: Path, text: str) -> Path:
    path = _canon_path(vault)
    if not path.exists():
        raise VaultError("canon.md missing")
    body = path.read_text(encoding="utf-8")
    new_body, count = _replace_lore_line(body, text, replacement=None)
